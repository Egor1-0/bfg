@main_router.message(F.text.lower() == '–∏–Ω–≤–µ–Ω—Ç–∞—Ä—å')
async def user_inventory(message: Message):
    inventory = await get_user_inventory(message.from_user.id)
    inventory_text = []

    for item in inventory:
        ore_name = (await get_ore_by_id(item.ore.lower())).ore
        amount = item.ammount_ore

        icon = ore_icon.get(ore_name)  #–ø–æ–ª—É—á–µ–Ω–∏–µ –∏–∫–æ–Ω–∫–∏ –ø–æ –∏–º–µ–Ω–∏ —Ä—É–¥—ã

        inventory_text.append(f"{icon} {ore_name}: {amount} —à—Ç")

    await message.answer(f"–í–∞—à –∏–Ω–≤–µ–Ω—Ç–∞—Ä—å:\n" + "\n".join(inventory_text))


